const JiemengPage = {
  init() {
    const form = document.getElementById('jiemengForm');
    if (form) form.addEventListener('submit', (e) => {
      e.preventDefault();
      const kw = document.getElementById('dreamKeyword').value.trim();
      if (!kw) { if (typeof Utils !== 'undefined') Utils.showToast('请输入梦境关键词'); return; }
      const result = Jiemeng.interpret(kw);
      const rs = document.getElementById('resultSection');
      if (rs) rs.style.display = 'block';
      const lw = document.getElementById('luckLevel');
      if (lw) { lw.textContent = result.jx; lw.className = `result-badge badge-${result.jx==='吉'?'gold':result.jx==='凶'?'red':'green'}`; }
      const dw = document.getElementById('dreamWord');
      if (dw) dw.textContent = kw;
      const jx = document.getElementById('jiXiong');
      if (jx) jx.textContent = result.jx;
      const tm = document.getElementById('traditionalMeaning');
      if (tm) tm.textContent = result.trad;
      const pm = document.getElementById('psychologyMeaning');
      if (pm) pm.textContent = result.psycho;
    });
  }
};
document.addEventListener('DOMContentLoaded', () => JiemengPage.init());